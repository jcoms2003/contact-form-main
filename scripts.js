// radio active color
function changeBg(general) {
  let change = document.getElementById(general);
  if ((change.checked = true)) {
    change.style.backgroundColor = "hsl(148, 38%, 91%)";
    change.style.borderColor = "hsl(169, 82%, 27%)";
  }
}
function changebG(support) {
  let changeTwo = document.getElementById(support);
  if ((changeTwo.checked = true)) {
    changeTwo.style.backgroundColor = "hsl(148, 38%, 91%)";
    changeTwo.style.borderColor = "hsl(169, 82%, 27%)";
  }
}
